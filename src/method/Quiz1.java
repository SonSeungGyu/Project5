package method;

public class Quiz1 {
	
	
	public static void cal(int a, int b) {
		System.out.println(a + " + " + b + " = " + (a + b));
		System.out.println(a + " - " + b + " = " + (a - b));
		System.out.println(a + " * " + b + " = " + (a * b)); 
		System.out.println(a + " / " + b + " = " + (a / b));
		System.out.println(a + " % " + b + " = " + (a % b));
		
	}

	
	public static void main(String[] args) {
		cal(50,2);
	}
	
}
